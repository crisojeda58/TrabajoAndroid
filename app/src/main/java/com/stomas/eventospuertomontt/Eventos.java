package com.stomas.eventospuertomontt;

public class Eventos {
    String nombre, organizador, fecha, hora, direccion, descripcion;

    public Eventos(String nombre, String organizador, String fecha, String hora, String direccion, String descripcion) {
        this.nombre = nombre;
        this.organizador = organizador;
        this.fecha = fecha;
        this.hora = hora;
        this.direccion = direccion;
        this.descripcion = descripcion;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getOrganizador() {
        return organizador;
    }

    public void setOrganizador(String organizador) {
        this.organizador = organizador;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public String getHora() {
        return hora;
    }

    public void setHora(String hora) {
        this.hora = hora;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }
}
